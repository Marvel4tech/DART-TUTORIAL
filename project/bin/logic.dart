void main() {
  //LOGIC - IF, ELSE, ELSE IF

  // if logic
  const number = 7;
  if (number > 5) {
    print("$number is bigger and you should get it");
  }

  // if else logic
  var firstName = "Marvellous";
  if (firstName == "Sandra") {
    print("The name is wrong");
  } else {
    print("The name is right");
  }

  // if else logic
  double weight = 63.00;
  if (weight == 34.00) {
    print("The Gas is half filled");
  } else if (weight == 63.00) {
    print("Gas is full");
  } else {
    print("No gas at all");
  }
}
