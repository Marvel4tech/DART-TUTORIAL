void main() {
  // var
  var name = "Godswill";
  print(name);

  // dynamic
  dynamic age = 30;
  print(age);

  // String
  String? city;
  print(city);

  city = "Delta";
  print(city);

  // integar
  int? weight;
  print(weight);

  // Const
  const food = "Eba";
  print(food);

  // final
  final state = "Lagos";
  print(state);
}
